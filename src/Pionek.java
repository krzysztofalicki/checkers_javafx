public class Pionek {
    public double x = 10;
    public double y = 10;
    public int rodzaj = 0;
    public boolean akt = false;
    public boolean widocznosc = false;

    public Pionek(double x, double y, int rodzaj, boolean akt, boolean widocznosc) {
        this.x = x;
        this.y = y;
        this.rodzaj = rodzaj;
        this.akt = akt;
        this.widocznosc = widocznosc;
    }
}
